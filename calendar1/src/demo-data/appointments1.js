import RoomBookingService from "../Services/RoomBookingService";

export default RoomBookingService.getEvents().then((response) => {
    const results = response.data;
    const resultArray = results.map(el => {
        return {
            allDay: false,
            title: undefined,
            startDate: new Date(el.startTime),
            endDate: new Date(el.endTime),
            id: parseInt(el.id), roomId: parseInt(el.commonRoom.id), residentId: parseInt(el.booker.id)
        }
    })
    console.log('1 => ',resultArray);
    return resultArray
});

